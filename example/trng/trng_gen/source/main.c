/**
 ******************************************************************************
 ** \brief  Main function of project
 **
 ** \return uint32_t return value, if needed
 **
 ** This sample
 **
 ******************************************************************************/

int32_t main(void)
{       
    ///< 打开RNG模块时钟门控
    Sysctrl_SetPeripheralGate(SysctrlPeripheralRng, TRUE);
    
    ///< 上电第一次生成随机数必须使用初始化函数生成并获取随机数
    ///< 上电第一次随机数生成并获取
    Trng_Init();
    
    pu32Data0[0] = Trng_GetData0();
    pu32Data0[1] = Trng_GetData1();
    
    ///< 随机数不应该为全0或全1
    if ( (pu32Data0[0] != 0) &&
         (pu32Data0[1] != 0) &&
         (pu32Data0[0] != 0xFFFFFFFF) &&
         (pu32Data0[1] != 0xFFFFFFFF))
    {
        ;
    }
    else
    {
        while(1);
    }
    
    ///< 如果在使用中有多次生成随机数的需要，则在第一次生产随机数后通过如下流程实现和获取随机数
    ///< 非上电第一次随机数生成并获取
    Trng_Generate();
    
    ///< 两次生产的随机数不相同
    if (pu32Data0[0] != Trng_GetData0())
    {
        if(pu32Data0[1] != Trng_GetData1())
        {
            ;
        }
        else
        {
            while(1);
        }

    }
    else
    {
        while(1);
    }
    
    while (1);
}

